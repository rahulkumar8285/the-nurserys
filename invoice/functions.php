// get invoice list
function getInvoices() {

	// Connect to the database
	$mysqli = new mysqli(DATABASE_HOST, DATABASE_USER, DATABASE_PASS, DATABASE_NAME);

	// output any connection error
	if ($mysqli->connect_error) {
		die('Error : ('.$mysqli->connect_errno .') '. $mysqli->connect_error);
	}

	// the query
    $query = "SELECT * FROM `invoices` ORDER BY `id` DESC";
	// mysqli select query
	$results = $mysqli->query($query);


	// mysqli select query
	if($results) {

		print '<table class="table table-striped table-hover table-bordered" id="data-table" cellspacing="0"><thead><tr>
				<th>S.No</th>
				<th>Invoice Num</th>
				<th>Invoice Type</th>
				<th>Customer</th>
				<th>Issue Date</th>
				<th>Total</th>
				<th>Actions</th>

			  </tr></thead><tbody>';

		while($row = $results->fetch_assoc()) {

			print '
				<tr>
					<td>'.$row["id"].'</td>
					<td>'.$row["invoice_num"].'</td>
				    <td>'. str_replace("_"," ",$row["invoice_type"]).'</td>
					<td>'.$row["customer_name"].'</td>
				    <td>'.date('d-M-Y',strtotime($row["invoice_date"])).'</td>
				    <td>'.$row["invoice_total"].'</td>

				';


			print '
				    <td><a href="invoice-edit.php?id='.$row["id"].'" class="btn btn-primary btn-xs"><span class="glyphicon glyphicon-edit" aria-hidden="true"></span></a>
					<a href="invoice_pdf.php?invoise_id='.$row['id'].'" class="btn btn-success btn-xs download_quotation "><span class="glyphicon glyphicon-download-alt" aria-hidden="true"></span></a>
					</td>
			    </tr>
			';

		}

		print '</tr></tbody></table>';

	} else {

		echo "<p>There are no invoices to display.</p>";

	}

	// Frees the memory associated with a result
	$results->free();

	// close connection 
	$mysqli->close();

}

